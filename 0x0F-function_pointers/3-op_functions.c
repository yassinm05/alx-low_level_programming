#include "3-calc.h"

/**
 * op_add - prints a name as is
 *
 * @a: param
 *
 * @b: param
 *
 * Return: Nothing.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - prints a name as is
 *
 * @a: param
 *
 * @b: param
 *
 * Return: Nothing.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - prints a name as is
 *
 * @a: param
 *
 * @b: param
 *
 * Return: Nothing.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - prints a name as is
 *
 * @a: param
 *
 * @b: param
 *
 * Return: Nothing.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - prints a name as is
 *
 * @a: param
 *
 * @b: param
 *
 * Return: Nothing.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
